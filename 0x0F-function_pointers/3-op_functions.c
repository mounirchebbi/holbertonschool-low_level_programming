#include <stdlib.h>
#include "calc.h"
/**
  *op_add - addition
  *@a: first int
  *@b: second int
  *Return: int
  */
int op_add(int a, int b)
{
	return (a + b);
}
/**
  *op_sub - substract
  *@a: first int
  *@b: second int
  *Return: int
  */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
  *op_mul - multiply
  *@a: first int
  *@b: second int
  *Return: int
  */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
  *op_div - divide
  *@a: first int
  *@b: second int
  *Return: int
  */
int op_div(int a, int b)
{
	return (a / b);
}
/**
  *op_mod - modulo
  *@a: first int
  *@b: second int
  *Return: int
  */
int op_mod(int a, int b)
{
	return (a % b);
}
